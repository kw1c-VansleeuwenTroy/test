void _Font::Free(void)
{
  _UWORD i;

  LseLabel.Free();

  for(i=0; i<No; i++)
    free_FONTINFO(&Ptr[i]);

  free_FONTINFO(&DefaultFont);

  FontFileName.Free();
  Memory2((void**)&Ptr, 0);

  Init();
}

_UDWORD _Font::LoadFont(_ASCII *dpne, _UWORD ino, _ATTRIB attrib)
{
  _DWORD  size;
  _UDWORD retcode = DEFSCOPE;

  size = (No + 1) * sizeof(_FONTINFO);

  if(Memory2((void**)&Ptr, size) != 0)
  {
    retcode = No;
    
    init_FONTINFO(&Ptr[No]);
    Ptr[No]        = (ino < No)?  Ptr[ino] : DefaultFont;
    Ptr[No].isheap = _FALSE;
    Ptr[No].size   = 0;

    if((dpne != NULL) && (ino >= No))
    {
      _FONTINFO tfi;
      init_FONTINFO(&tfi);
      if(LoadFontFile(&tfi, dpne) == _TRUE)
      {
        Ptr[No] = tfi;
      }
    }

    Ptr[No].attrib = attrib & (T_COPY|T_SOLID|T_PROP|T_BOLD|T_STRIKEOUT|T_ITALIC|T_UNDERLINE);

    No ++;
    GetSizeDirect(&Ptr[No-1].width, &Ptr[No-1].height, No-1);
  }

  return retcode;
}

_BOOL _Font::Load(_ASCII *dpne)
{
  _File       File;
  _BOOL       retcode = _FALSE;
  _UWORD      offset, i, j;
  _ASCII      path[_MAX_PATHLENGTH], *iname;
  _UWORD      ino;
  _MEM_ATTRIB ma;

  Free();
  init_MEM_ATTRIB(&ma);

  File.Open(LOAD, dpne);
  if(File.ReadHeader() == _TRUE)
  {
    if(FontFileName.Read(&File, _FALSE) == _TRUE)
    {
      File.Read_MEM_ATTRIB(&ma); // COMPILER_VERSION 20
    }

   #if COMPILER_VERSION >= 33
    LseLabel.Read(&File, _FALSE);
   #endif

    retcode = (File.GetError() == _FALSE)? _TRUE : _FALSE;
  }
  File.Close();

  if(retcode == _TRUE)
  {
    StrCpy(path, sizeof(_ASCII), dpne, sizeof(_ASCII));
    offset = offset_dpne(path, 1);

    for(i=0;i<FontFileName.GetNo(); i++)
    {
      iname = FontFileName.GetText(i);
      ino   = 0xffff;
      if(i > 0)
      {
        for(j=0; j<i; j++)
        {
          if(StrXmp(FontFileName.GetText(j), iname, sizeof(_ASCII)) == 0)
          {
            ino = j;
            break;
          }
        }
      }

      path[offset] = 0;
      StrCat(path, sizeof(_ASCII), iname, sizeof(_ASCII));
      LoadFont(path, ino, (i < ma.no)? ma.ptr[i] : T_COPY|T_SOLID|T_PROP);
    }
  }

  free_MEM_ATTRIB(&ma);
  
// #if COMPILER_VERSION >= 33
//  if(LseLabel.Add((_ASCII*)"_@Default") == _TRUE)
//  {
//    LoadFont(NULL, 0xffff, T_COPY|T_SOLID|T_PROP);
//    GetSizeDirect(&DefaultFont.width, &DefaultFont.height, 0xFFFF);
//  }
// #endif

//  Set(DEFFONT, NULL);
  SetDirect(DEFFONT, NULL);

  return(retcode);
}

