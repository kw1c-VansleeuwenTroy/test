_UDWORD _Font::Add(void *fontptr, _ASCII *label, _ATTRIB attrib, _BOOL isheap)
{
  _UDWORD retcode = DEFSCOPE;
  
  if(LseLabel.Seek(&retcode, label) == _TRUE)
    return retcode;

  if(fontptr == NULL)
  {
    #ifdef UC_IPC
     fontptr = MyDefaultFont;
     isheap = _FALSE;
    #else
     return DEFSCOPE;
    #endif
  }

  if(Memory2((void**)&Ptr, (No + 1) * sizeof(_FONTINFO)) != 0)
  {
    if(LseLabel.Add(label) == _TRUE)
    {
      init_FONTINFO(&Ptr[No]);
      Ptr[No].ptr    = (_FONTPTR*)fontptr;
      Ptr[No].isheap = isheap;
      Ptr[No].attrib = attrib;
       
      No += 1;
          
      GetSizeDirect(&Ptr[No-1].width, &Ptr[No-1].height, No-1);        
      return (No - 1);
    }
  }
    
  return DEFSCOPE;
}


